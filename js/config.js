function alleFinden(attribute){
	var menge = new Set()

	for (var [key,lab] of Object.entries(getLabs())) {
		for (var attr of lab[attribute]) {
			if (!attr) continue;
			menge.add(attr);
		}
	}
	return Array.from(menge);
}

